async function call(model: string, system: string, user: string, maxTokens: number) {
  const res = await fetch('https://openrouter.ai/api/v1/chat/completions', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json', Authorization: `Bearer ${key}`, 'X-Title': 'hiagents-eval' },
    body: JSON.stringify({
      model, temperature: 0, max_tokens: maxTokens,
      usage: { include: true },
      messages: [{ role: 'system', content: system }, { role: 'user', content: user }],
    }),
  });
  const json = await res.json();
  if (!res.ok) throw new Error(`${model}: ${res.status}`);
  return { content: json.choices?.[0]?.message?.content ?? '', cost: json.usage?.cost ?? 0 };
}

