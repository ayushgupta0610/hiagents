const MODELS = [
  'openai/gpt-4o-mini',
  'mistralai/mistral-small-24b-instruct-2501',
];

console.log('Model'.padEnd(45) + 'Pass / Total (critical fails)  Cost');
console.log('-'.repeat(85));
for (const model of MODELS) {
  let pass = 0; let critFail = 0; let totalCost = 0;
  const fails: string[] = [];
  for (const c of CASES) {
    try {
      const { content, cost } = await call(model, c.sys, c.user, c.maxTokens);
      totalCost += cost;
      const trimmed = content.trim();
      const firstToken = trimmed.split(/[|\s]/)[0]?.toUpperCase() ?? '';
      const matched = firstToken === c.want;
      if (matched) pass += 1;
      else {
        if (c.critical) critFail += 1;
        const flag = c.critical ? '🚨' : '  ';
        fails.push(`    ${flag} ${c.name.padEnd(22)} want=${c.want.padEnd(12)} got=${JSON.stringify(content).slice(0, 70)}`);
      }
    } catch (e) {
      if (c.critical) critFail += 1;
      fails.push(`    ✗ ${c.name.padEnd(22)} ERROR: ${e instanceof Error ? e.message.slice(0, 60) : e}`);
    }
  }
  console.log(`${model.padEnd(45)}${String(pass).padStart(3)} / ${CASES.length}  (${critFail} critical fails)  $${totalCost.toFixed(6)}`);
  for (const f of fails) console.log(f);
  console.log();
}
